import AppKit

@MainActor
final class CommandKeyState: ObservableObject {
    static let shared = CommandKeyState()
    
    func getKeyDescription(forKeycode keycode: Int64) -> String {
        // Mapeia alguns keycodes comuns
        switch keycode {
        case 0: return "A"
        case 1: return "S"
        case 2: return "D"
        case 3: return "F"
        case 4: return "H"
        case 5: return "G"
        case 6: return "Z"
        case 7: return "X"
        case 8: return "C"
        case 9: return "V"
        case 11: return "B"
        case 12: return "Q"
        case 13: return "W"
        case 14: return "E"
        case 15: return "R"
        case 16: return "Y"
        case 17: return "T"
        case 31: return "O"
        case 32: return "U"
        case 34: return "I"
        case 35: return "P"
        default: return "\(keycode)"
        }
    }
}
